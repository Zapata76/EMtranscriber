@dataclass(slots=True)
class UiTranslator:
    language: str

    def t(self, key: str, **kwargs) -> str:
        bucket = TRANSLATIONS.get(key)
        if bucket is None:
            return key
        text = bucket.get(self.language) or bucket.get("en") or key
        if kwargs:
            return text.format(**kwargs)
        return text


def detect_system_language() -> str:
    candidates = [
        locale.getlocale()[0],
        locale.getdefaultlocale()[0] if hasattr(locale, "getdefaultlocale") else None,
    ]
    for value in candidates:
        if not value:
            continue
        lang = value.split("_")[0].lower().strip()
        if lang in SUPPORTED_UI_LANGUAGES:
            return lang
    return "en"


def resolve_ui_language(configured: str | None) -> str:
    if configured:
        candidate = configured.strip().lower()
        if candidate in SUPPORTED_UI_LANGUAGES:
            return candidate
        return "en"

    system_lang = detect_system_language()
    if system_lang in SUPPORTED_UI_LANGUAGES:
        return system_lang
    return "en"



